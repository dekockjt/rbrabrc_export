
--UNDERGRAD TRANSFER PS,PL,NR041,TEAC03, NR06 ENROLLED WITH SGASTDN AND CHANGED SGASTDN RECORD AFTER FIRST TERM
SELECT CASE
    WHEN NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, Y.SGBSTDN_PIDM, 'N'), 0) >= RORCRHR_HALF_TIME_CR_HRS
        THEN (RORALGS_AMT + 300)
    END as calc_amt
FROM SGBSTDN X, SGBSTDN Y, RORCRHR, RORALGS, ROBNYUD
WHERE RORALGS_KEY_1 = 'PBDG'
AND RORALGS_KEY_4 = '6LIV'
AND CASE -- transfer student type
    WHEN X.SGBSTDN_STYP_CODE = 'T' THEN 'T' 
END = RORALGS_KEY_2
AND CASE -- OFF CAMPUS HOUSING FLAG IN ROBNYUD
    WHEN ROBNYUD_VALUE_120 IS NULL THEN '1'  --off campus 
	WHEN (ROBNYUD_VALUE_120 NOT IN ('C','D')) THEN '1'  --off campus 
	WHEN ROBNYUD_VALUE_120 = 'D' THEN '3'  --with family/relative
END = RORALGS_KEY_3	
AND CASE 
    WHEN Y.SGBSTDN_LEVL_CODE = 'UG' THEN 'UG'
END = RORALGS_KEY_5

AND CASE -- campus code
    WHEN Y.SGBSTDN_CAMP_CODE = 'FR' THEN 'FR'
END = RORALGS_KEY_6

-- max sgbstdn term <= passed period
AND Y.SGBSTDN_TERM_CODE_EFF = (
    SELECT MAX(Y1.SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN Y1
    WHERE Y1.SGBSTDN_PIDM = Y.SGBSTDN_PIDM
    AND Y1.SGBSTDN_TERM_CODE_EFF <= :PERIOD
)

-- has transfer sgastdn record in the aid year	
AND X.SGBSTDN_TERM_CODE_EFF BETWEEN 
    TRIM(TO_CHAR ('20'||SUBSTR(:AIDY, 3,2) || '00')) 
    AND TRIM(TO_CHAR ('20'||SUBSTR(:AIDY, 3,2)+1 || '00'))	
AND X.SGBSTDN_STYP_CODE = 'T' 
AND X.SGBSTDN_STST_CODE IN  ('AS','IL','P1') 

AND Y.SGBSTDN_TERM_CODE_EFF > X.SGBSTDN_TERM_CODE_EFF
AND Y.SGBSTDN_LEVL_CODE = 'UG'
AND Y.SGBSTDN_STST_CODE IN  ('AS','IL','P1')
AND RORCRHR_AIDY_CODE = :AIDY
AND RORCRHR_LEVL_CODE = Y.SGBSTDN_LEVL_CODE
AND RORCRHR_PERIOD = :PERIOD
AND (
    X.SGBSTDN_COLL_CODE_1 IN ('PS','PL') OR 
    X.SGBSTDN_PROGRAM_1  IN ('NR041','NR02','TEAC03','NR06')
)            
AND X.SGBSTDN_PIDM = ROBNYUD_PIDM 
AND X.SGBSTDN_PIDM = Y.SGBSTDN_PIDM
AND X.SGBSTDN_PIDM = :PIDM
AND RORALGS_AIDY_CODE = :AIDY
-- END
