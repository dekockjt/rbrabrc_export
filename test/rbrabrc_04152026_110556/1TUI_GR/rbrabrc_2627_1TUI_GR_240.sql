
--GRAD SE, BA, PS, G% ATTRIBUTES WITH SGASTDN
--DON'T NEED SEQUENCE FOR ENROLLED SINCE IT'S ALWAYS A FLAT RATE
SELECT RORALGS_AMT
FROM SGBSTDN X, SGRSATT X, RORALGS
WHERE RORALGS_KEY_1 = 'PBDG'

AND RORALGS_KEY_4 = '1TUI' -- budget component

AND CASE -- level code
    WHEN X.SGBSTDN_LEVL_CODE = 'GR' THEN 'GR'
END = RORALGS_KEY_5

AND CASE -- campus code
    WHEN X.SGBSTDN_CAMP_CODE = 'FR' THEN 'FR'
END = RORALGS_KEY_6

AND CASE -- college code
    WHEN X.SGBSTDN_COLL_CODE_1 = 'SE' THEN 'SE'
    WHEN X.SGBSTDN_COLL_CODE_1 = 'BA' THEN 'BA'
	WHEN X.SGBSTDN_COLL_CODE_1 = 'PS' THEN 'PS'
END = RORALGS_KEY_7

AND CASE -- cohort code
    WHEN X.SGRSATT_ATTS_CODE = 'GG2' THEN 'GG2'
    WHEN X.SGRSATT_ATTS_CODE = 'GG3' THEN 'GG3'
	WHEN X.SGRSATT_ATTS_CODE = 'GG4' THEN 'GG4'
	WHEN X.SGRSATT_ATTS_CODE = 'GG5' THEN 'GG5'
	WHEN X.SGRSATT_ATTS_CODE = 'GGI2' THEN 'GGI2'
	WHEN X.SGRSATT_ATTS_CODE = 'GGI3' THEN 'GGI3'
END = RORALGS_KEY_10

-- max sgbstdn term <= passed period
AND X.SGBSTDN_TERM_CODE_EFF = (
    SELECT MAX(Y.SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN Y
    WHERE Y.SGBSTDN_PIDM = X.SGBSTDN_PIDM
    AND Y.SGBSTDN_TERM_CODE_EFF <= :PERIOD
)
	--max attribute term <= passed term
AND X.SGRSATT_TERM_CODE_EFF = (
SELECT MAX(Y.SGRSATT_TERM_CODE_EFF)
    FROM SGRSATT Y
    WHERE Y.SGRSATT_TERM_CODE_EFF <= :PERIOD
	AND X.SGRSATT_PIDM =  Y.SGRSATT_PIDM
)
AND NOT EXISTS (
    SELECT 1
    FROM SARADAP
    INNER JOIN SARAPPD
        ON SARAPPD_PIDM = SARADAP_PIDM
        AND SARAPPD_APPL_NO = SARADAP_APPL_NO
        AND SARAPPD_TERM_CODE_ENTRY = SARADAP_TERM_CODE_ENTRY
    INNER JOIN STVAPDC
        ON STVAPDC_CODE = SARAPPD_APDC_CODE
        AND STVAPDC_INST_ACC_IND = 'Y'
        AND STVAPDC_SIGNF_IND = 'Y'
    WHERE SARADAP_PIDM = X.SGBSTDN_PIDM
    AND SARADAP_TERM_CODE_ENTRY > X.SGBSTDN_TERM_CODE_EFF 
    AND SARADAP_TERM_CODE_ENTRY <= ('20' || cast(substr(:AIDY, 2, 2) as int) + 1 || '00')
    AND SARADAP_PROGRAM_1 <> X.SGBSTDN_PROGRAM_1
)
AND X.SGBSTDN_STST_CODE IN  ('AS','IL','P1')
AND X.SGBSTDN_PIDM = X.SGRSATT_PIDM
AND X.SGBSTDN_PIDM = :PIDM
AND RORALGS_AIDY_CODE = :AIDY
--END
