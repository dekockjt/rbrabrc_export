
SELECT RORALGS_AMT * 2
    FROM SARADAP a, SARAPPD b, STVAPDC c, RORALGS
WHERE RORALGS_KEY_1 = 'PBDG'
AND RORALGS_KEY_4 = '8TRS' -- budget component
AND RORALGS_KEY_5 = 'UG'
-- max saradap term <= passed period
AND a.SARADAP_TERM_CODE_ENTRY = (
    SELECT MAX(SARADAP_TERM_CODE_ENTRY)
    FROM SARADAP, ROBINST
    WHERE SARADAP_PIDM = a.SARADAP_PIDM
    AND SARADAP_TERM_CODE_ENTRY >= (ROBINST_AIDY_END_YEAR || '00')   	
	AND SARADAP_TERM_CODE_ENTRY <= :PERIOD
	AND ROBINST_AIDY_CODE = :AIDY   
)
--max application decision code sequence number
AND b.SARAPPD_SEQ_NO = (
    SELECT MAX (SARAPPD_SEQ_NO)
    FROM SARAPPD, ROBINST
    WHERE SARAPPD_PIDM = b.SARAPPD_PIDM
    AND SARAPPD_SEQ_NO < 99
    AND SARAPPD_TERM_CODE_ENTRY >= (ROBINST_AIDY_END_YEAR || '00')
    AND SARAPPD_TERM_CODE_ENTRY <= :PERIOD
    AND ROBINST_AIDY_CODE = :AIDY           
    AND SARADAP_APPL_NO = b.SARAPPD_APPL_NO
)  
AND c.STVAPDC_INST_ACC_IND = 'Y'
AND c.STVAPDC_SIGNF_IND = 'Y'
AND b.SARAPPD_APDC_CODE = c.STVAPDC_CODE	
AND a.SARADAP_APPL_NO = b.SARAPPD_APPL_NO
AND a.SARADAP_TERM_CODE_ENTRY = b.SARAPPD_TERM_CODE_ENTRY
AND RORALGS_AIDY_CODE = :AIDY
AND b.SARAPPD_PIDM = a.SARADAP_PIDM	
AND a.SARADAP_PIDM = :PIDM
