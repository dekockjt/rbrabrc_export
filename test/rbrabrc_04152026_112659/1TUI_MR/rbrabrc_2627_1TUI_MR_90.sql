
--SPSA ENROLLED 19+ HOURS 
SELECT CASE
    WHEN NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, X.SGBSTDN_PIDM, 'N'), 0) > 18
        THEN round(roralgs_amt + ((NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, X.SGBSTDN_PIDM, 'N'), 0) - 18) * roralgs_comment_12)) 
    END as calc_amt

--need to add text to RORALGS_COMMENT SAYING DO NOT CHANGE TEXT IN RORALGS_COMMENT_12 BECASUE IT'S USED IN THE ALGO CALC
--VALUE TO ADD TO ADD TO RORALGS_COMMENT_12 IS OVERLOAD RATE, ALL NUMERIC, WOULD BE 1830 BASED ON FY2024 RATE

FROM SGBSTDN X, RORALGS, SGRSATT X
WHERE RORALGS_KEY_1 = 'PBDG'

AND RORALGS_KEY_4 = '1TUI' -- budget component

AND CASE -- level code
    WHEN X.SGBSTDN_LEVL_CODE = 'UG' THEN 'UG'
END = RORALGS_KEY_5

AND CASE -- campus code
    WHEN X.SGBSTDN_CAMP_CODE = 'FR' THEN 'FR'
END = RORALGS_KEY_6

--attribute for Madrid study abroad
AND CASE -- attribute code
    WHEN X.SGRSATT_ATTS_CODE = 'SPSA' THEN 'SPSA'
END = RORALGS_KEY_10

AND RORALGS_KEY_12 = '19+'

--max attribute term <= passed term	
AND X.SGRSATT_TERM_CODE_EFF = (
SELECT MAX(Y.SGRSATT_TERM_CODE_EFF)
    FROM SGRSATT Y
    WHERE Y.SGRSATT_TERM_CODE_EFF <= :PERIOD
	AND X.SGRSATT_PIDM =  Y.SGRSATT_PIDM
)
-- max sgbstdn term <= passed period
AND X.SGBSTDN_TERM_CODE_EFF = (
    SELECT MAX(Y.SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN Y
    WHERE Y.SGBSTDN_PIDM = X.SGBSTDN_PIDM
    AND Y.SGBSTDN_TERM_CODE_EFF <= :PERIOD
)
AND X.SGBSTDN_STST_CODE IN  ('AS','IL','P1')
AND X.SGBSTDN_PIDM = X.SGRSATT_PIDM
AND X.SGBSTDN_PIDM = :PIDM
AND RORALGS_AIDY_CODE = :AIDY
-- END
