
--UNDERGRAD PS,PL,NR041,TEAC03, NR06 WITH SGASTDN
SELECT (RORALGS_AMT + 300)
FROM SGBSTDN X, RORALGS, RORPRST
WHERE RORALGS_KEY_1 = 'PBDG'
AND RORALGS_KEY_4 = '6LIV'
AND CASE -- OFF CAMPUS HOUSING FLAG IN RORPRST
	WHEN (X.SGBSTDN_STYP_CODE = 'F' and RORPRST_XHS_LOCK_IND = 'Y' and RORPRST_XHS = '3') THEN '1' -- off campus
	WHEN (X.SGBSTDN_STYP_CODE = 'F' and RORPRST_XHS_LOCK_IND = 'Y' and RORPRST_XHS in ('1', '4')) THEN '3' -- family/relative
	WHEN (X.SGBSTDN_STYP_CODE <> 'F' and RORPRST_XHS = '3') THEN '1' -- off campus
	WHEN (X.SGBSTDN_STYP_CODE <> 'F' and RORPRST_XHS in ('1', '4')) THEN '3' -- family/relative
END = RORALGS_KEY_3
AND CASE -- level code
    WHEN X.SGBSTDN_LEVL_CODE = 'UG' THEN 'UG'
END = RORALGS_KEY_5	
AND CASE -- campus code
    WHEN X.SGBSTDN_CAMP_CODE = 'FR' THEN 'FR'
END = RORALGS_KEY_6	
AND X.SGBSTDN_TERM_CODE_EFF = (
    SELECT MAX(Y.SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN Y
    WHERE Y.SGBSTDN_PIDM = X.SGBSTDN_PIDM
    AND Y.SGBSTDN_TERM_CODE_EFF <= :PERIOD
)
AND (
    X.SGBSTDN_COLL_CODE_1 IN ('PS','PL')                           --check with SLU to confirm this.  Look for these or NOT these?
    OR X.SGBSTDN_PROGRAM_1  IN ('NR041','NR02','TEAC03', 'NR06')
)
AND X.SGBSTDN_PIDM = RORPRST_PIDM
AND RORPRST_PERIOD = :PERIOD
AND X.SGBSTDN_STST_CODE IN  ('AS','IL','P1')
AND X.SGBSTDN_PIDM = :PIDM
AND RORALGS_AIDY_CODE = :AIDY
-- END
