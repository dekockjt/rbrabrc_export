
--UNDERGRAD FRESHMEN ENROLLED WITH SGASTDN
SELECT CASE
    WHEN NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, X.SGBSTDN_PIDM, 'N'), 0) >= RORCRHR_HALF_TIME_CR_HRS
        THEN RORALGS_AMT
    END as calc_amt
FROM SGBSTDN X, RORCRHR, RORALGS, RORPRST
WHERE RORALGS_KEY_1 = 'PBDG'

AND CASE -- freshmen student type
    WHEN (X.SGBSTDN_STYP_CODE IN ('F','1')) THEN 'F'                   
END = RORALGS_KEY_2 

AND RORALGS_KEY_4 = '3HSM' -- budget component                        

AND CASE -- level code
    WHEN X.SGBSTDN_LEVL_CODE = 'UG' THEN 'UG'
END = RORALGS_KEY_5

AND CASE -- campus code
    WHEN X.SGBSTDN_CAMP_CODE = 'FR' THEN 'FR'
END = RORALGS_KEY_6

-- max sgbstdn term <= passed period
AND X.SGBSTDN_TERM_CODE_EFF = (
    SELECT MAX(SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN
    WHERE SGBSTDN_PIDM = X.SGBSTDN_PIDM
    AND SGBSTDN_TERM_CODE_EFF <= :PERIOD
)
AND RORCRHR_AIDY_CODE = :AIDY
AND RORCRHR_LEVL_CODE = X.SGBSTDN_LEVL_CODE
AND RORCRHR_PERIOD = :PERIOD

AND (RORPRST_XHS_LOCK_IND <> 'Y' OR RORPRST_XHS IS NULL)  
AND X.SGBSTDN_PIDM = RORPRST_PIDM
AND RORPRST_PERIOD = :PERIOD
AND X.SGBSTDN_STST_CODE IN  ('AS','IL','P1')
AND X.SGBSTDN_PIDM = :PIDM
AND RORALGS_AIDY_CODE = :AIDY
-- END

