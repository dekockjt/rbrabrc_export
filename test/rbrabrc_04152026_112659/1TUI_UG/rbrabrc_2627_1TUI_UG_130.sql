
--UG RN2BSN MAJOR ENROLLED 
SELECT roralgs_amt * ( case when NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, X.SGBSTDN_PIDM, 'N'), 0) <= 0 then 12 else NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, X.SGBSTDN_PIDM, 'N'), 0) end)

FROM SGBSTDN X, RORALGS
WHERE RORALGS_KEY_1 = 'PBDG'

AND RORALGS_KEY_4 = '1TUI' -- budget component

AND CASE -- level code
    WHEN X.SGBSTDN_LEVL_CODE = 'UG' THEN 'UG'
END = RORALGS_KEY_5

AND CASE -- campus code
    WHEN X.SGBSTDN_CAMP_CODE = 'FR' THEN 'FR'
END = RORALGS_KEY_6

AND CASE -- college code
    WHEN X.SGBSTDN_COLL_CODE_1 = 'NR' THEN 'NR'
    WHEN X.SGBSTDN_COLL_CODE_1 = 'ED' THEN 'ED'
END = RORALGS_KEY_7

AND CASE -- major/program code
    WHEN X.SGBSTDN_PROGRAM_1 IN ('NR041','NR042','NR043','NR044','NR09') THEN 'RN2BSN'
    WHEN X.SGBSTDN_PROGRAM_1 = 'TEAC03' THEN 'TEAC03'
END = RORALGS_KEY_9		

-- max sgbstdn term <= passed period
AND X.SGBSTDN_TERM_CODE_EFF = (
    SELECT MAX(Y.SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN Y
    WHERE Y.SGBSTDN_PIDM = X.SGBSTDN_PIDM
    AND Y.SGBSTDN_TERM_CODE_EFF <= :PERIOD
)
AND X.SGBSTDN_STST_CODE IN  ('AS','IL','P1')
AND X.SGBSTDN_PIDM = :PIDM
AND RORALGS_AIDY_CODE = :AIDY
-- END

