
SELECT RORALGS_AMT
FROM SARADAP a
CROSS JOIN RORALGS
INNER JOIN SARAPPD b
    ON b.SARAPPD_PIDM = a.SARADAP_PIDM
    AND b.SARAPPD_APPL_NO = a.SARADAP_APPL_NO
    AND b.SARAPPD_TERM_CODE_ENTRY = a.SARADAP_TERM_CODE_ENTRY
INNER JOIN STVAPDC c
    ON c.STVAPDC_CODE = b.SARAPPD_APDC_CODE
    AND c.STVAPDC_INST_ACC_IND = 'Y'
    AND c.STVAPDC_SIGNF_IND = 'Y'
INNER JOIN ROBINST R on R.ROBINST_AIDY_CODE = RORALGS_AIDY_CODE
INNER JOIN RBRAPBG bg
ON bg.RBRAPBG_PIDM = a.SARADAP_PIDM
    AND bg.RBRAPBG_PBGP_CODE = 'MR'
    AND bg.RBRAPBG_PERIOD = :PERIOD
    AND bg.RBRAPBG_RUN_NAME = 'ACTUAL'
 
WHERE RORALGS_AIDY_CODE = :AIDY
AND a.SARADAP_PIDM = :PIDM
AND RORALGS_KEY_1 = 'PBDG'
AND RORALGS_KEY_4 = '5CAF'
AND CASE
    WHEN a.SARADAP_LEVL_CODE = 'UG' THEN 'UG'
END = RORALGS_KEY_5
AND CASE
    WHEN a.SARADAP_CAMP_CODE = 'SP' THEN 'SP'
END = RORALGS_KEY_6
AND a.SARADAP_TERM_CODE_ENTRY = (
    SELECT MAX(SARADAP_TERM_CODE_ENTRY)
    FROM SARADAP
    WHERE SARADAP_PIDM = a.SARADAP_PIDM
	AND SARADAP_TERM_CODE_ENTRY BETWEEN (R.ROBINST_AIDY_END_YEAR || '00') AND :PERIOD
)
AND b.SARAPPD_SEQ_NO = (
    SELECT MAX(SARAPPD_SEQ_NO)
    FROM SARAPPD
    WHERE SARAPPD_PIDM = b.SARAPPD_PIDM
    AND SARAPPD_SEQ_NO < 99
    AND SARAPPD_TERM_CODE_ENTRY = a.SARADAP_TERM_CODE_ENTRY           
    AND SARAPPD_APPL_NO = a.SARADAP_APPL_NO
)