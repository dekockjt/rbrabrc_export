
-- PKTF PERIOD BUDGET RULE - written by JD 10/3/2025
-- Parks Tech Fee (previously PARKS_TECH_FEE)
SELECT CASE
    -- full time
    WHEN NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, a.SGBSTDN_PIDM, 'N'), 0) >= 12 
        THEN RORALGS_AMT 
    -- part time
    WHEN NVL(ROKMISC.F_CALC_STUD_BILL_HRS(:PERIOD, a.SGBSTDN_PIDM, 'N'), 0)
        BETWEEN 1 AND 11 THEN ROUND((RORALGS_AMT / 2))
    END as calc_amt
FROM SGBSTDN a, RORALGS
WHERE RORALGS_KEY_1 = 'PBDG'
AND RORALGS_KEY_4 = '23PF' -- budget component
AND RORALGS_KEY_5 = 'UG' -- student level
AND CASE -- college code
    WHEN a.SGBSTDN_COLL_CODE_1 = 'SE' THEN 'SE'
END = RORALGS_KEY_7
AND a.SGBSTDN_TERM_CODE_EFF = ( -- max sgbstdn term <= passed period
    SELECT MAX(z.SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN z
    WHERE z.SGBSTDN_PIDM = a.SGBSTDN_PIDM
    AND z.SGBSTDN_TERM_CODE_EFF <= :PERIOD
)
AND a.SGBSTDN_CAMP_CODE = 'FR'
AND a.SGBSTDN_PIDM = :PIDM
-- END
