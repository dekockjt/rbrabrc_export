-- Commuter meal plan, component only included in UG COA group
-- UNDERGRAD TRANSFER WITH SGASTDN AND CHANGED SGASTDN FOR LATER TERM
SELECT RORALGS_AMT
FROM SGBSTDN X, SGBSTDN Y, RORALGS, RORPRST, ROBNYUD
WHERE RORALGS_KEY_1 = 'PBDG'

AND CASE
    WHEN X.SGBSTDN_STYP_CODE = 'T' AND ROBNYUD_VALUE_120 in ('D', '0', 'O') THEN 'T'
END = RORALGS_KEY_2

--AND CASE -- OFF CAMPUS HOUSING FLAG IN RORPRST
--    WHEN (RORPRST_XHS IN ('1','3','4')) THEN '1'     --off campus or with family/relative
--END = RORALGS_KEY_3

AND RORALGS_KEY_4 = '4COM' -- budget component                        

AND CASE -- level code
    WHEN Y.SGBSTDN_LEVL_CODE = 'UG' THEN 'UG'
END = RORALGS_KEY_5

AND CASE -- campus code
    WHEN Y.SGBSTDN_CAMP_CODE = 'FR' THEN 'FR'
END = RORALGS_KEY_6

-- max sgbstdn term <= passed period
AND Y.SGBSTDN_TERM_CODE_EFF = (
    SELECT MAX(Y1.SGBSTDN_TERM_CODE_EFF)
    FROM SGBSTDN Y1
    WHERE Y1.SGBSTDN_PIDM = Y.SGBSTDN_PIDM
    AND Y1.SGBSTDN_TERM_CODE_EFF <= :PERIOD)

AND Y.SGBSTDN_COLL_CODE_1 NOT IN ('PS','PL')
AND Y.SGBSTDN_PROGRAM_1 NOT IN ('NR041','NR02','TEAC03','NR06')            -- from JKB 11/17 added accel nursing, rising teacher

AND Y.SGBSTDN_PIDM = RORPRST_PIDM
AND RORPRST_PERIOD = :PERIOD	
AND Y.SGBSTDN_PIDM = ROBNYUD_PIDM
AND Y.SGBSTDN_STST_CODE IN  ('AS','IL','P1')
AND X.SGBSTDN_PIDM = Y.SGBSTDN_PIDM
AND Y.SGBSTDN_PIDM = :PIDM
AND RORALGS_AIDY_CODE = :AIDY

-- has freshmen sgastdn record in the aid year	
AND X.SGBSTDN_TERM_CODE_EFF BETWEEN TRIM(TO_CHAR ('20'||SUBSTR(:AIDY, 3,2) || '00')) AND TRIM(TO_CHAR ('20'||SUBSTR(:AIDY, 3,2)+1 || '00'))	
 AND X.SGBSTDN_STYP_CODE = 'T'
  AND X.SGBSTDN_STST_CODE IN  ('AS','IL','P1') 

  AND Y.SGBSTDN_TERM_CODE_EFF > X.SGBSTDN_TERM_CODE_EFF

AND NOT EXISTS (
    SELECT 1
    FROM SARADAP
    INNER JOIN SARAPPD
        ON SARAPPD_PIDM = SARADAP_PIDM
        AND SARAPPD_APPL_NO = SARADAP_APPL_NO
        AND SARAPPD_TERM_CODE_ENTRY = SARADAP_TERM_CODE_ENTRY
    INNER JOIN STVAPDC
        ON STVAPDC_CODE = SARAPPD_APDC_CODE
        AND STVAPDC_INST_ACC_IND = 'Y'
        AND STVAPDC_SIGNF_IND = 'Y'
    WHERE SARADAP_PIDM = X.SGBSTDN_PIDM
    AND SARADAP_TERM_CODE_ENTRY > X.SGBSTDN_TERM_CODE_EFF 
    AND SARADAP_TERM_CODE_ENTRY <= ('20' || cast(substr(:AIDY, 2, 2) as int) + 1 || '00')
    AND SARADAP_PROGRAM_1 <> X.SGBSTDN_PROGRAM_1
)
